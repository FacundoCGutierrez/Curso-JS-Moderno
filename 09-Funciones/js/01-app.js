
function sumar() {
    console.log(2 + 2);
}


sumar(); // se manda llamar por su nombre seguido del parentesis()


// Expresión de función - Este tipo de funciones se asigna como si fuera una variable
const sumar2 = function() {
    console.log(3 + 3);
}

sumar2(); // se manda llamar de la misma forma


